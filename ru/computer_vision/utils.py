import subprocess
from picamera import PiCamera

camera = PiCamera()

def get_photo(fp_out):
    """
    Make and save photo from picamera module
    fp_out : path for save photo
    return image path.
    """
    
    camera.start_preview()
    time.sleep(2)
    image_name = str(time.ctime()) + ".jpg"
    image_path = f"{fp_out}{image_name}"
    camera.capture(image_path)
    camera.stop_preview()
    return image_path


def view_result(images_path,
                filename, 
                n_sec=5):
    """
    Opens a new image viewing process.
    After n_sec kill process.

    images_path : directory path where images are stored.
    filename : name images for open.
    r
    """
    subprocess.Popen(f"gpicview {images_path}/{filename}",
                     shell=True)
    time.sleep(n_sec)
    subprocess.Popen("killall gpicview", shell=True)

